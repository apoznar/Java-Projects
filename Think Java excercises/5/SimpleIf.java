/** 
 * The Simple class implements an application that
 * checks if number under variable x is positive digit number.
 *
 * @author  Adam Poznar
 * @version 1.1
 * @since   2018-09-13
 */
public class SimpleIf {
     /** 
 * This main method sets variable x and checks if it's
 * positive single digit number.
 */
    public static void main(String[] args) {
        int x = 5;
        if (x > 0 && x < 10) {
            System.out.println("positive single digit number.");
        }
    }
}
